CREATE TABLE users
(

 USER_ID SERIAL NOT NULL,
 USER_NAME TEXT NOT NULL UNIQUE,
 -- TODO: ADD ENCRYPTION!
 USER_PASSWORD TEXT NOT NULL UNIQUE,

 PRIMARY KEY (USER_ID)

);
CREATE TABLE boxes
(
 BOX_ID SERIAL NOT NULL,
 BOX_POSITION INTEGER UNIQUE,

 PRIMARY KEY (BOX_ID)

);

CREATE TABLE palletes
(

 PALETTE_ID SERIAL NOT NULL,
 BOX_ID SERIAL REFERENCES boxes(BOX_ID),
 PALETTE_CONTENTS TEXT,

 PRIMARY KEY(PALETTE_ID)

);
